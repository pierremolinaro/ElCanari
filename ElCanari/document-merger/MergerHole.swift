final class MergerHole : EBSimpleClass {

  //····················································································································

  let x : Int
  let y : Int
  let holeDiameter : Int

  //····················································································································

  init (x inX : Int,
        y inY : Int,
        holeDiameter inHoleDiameter : Int) {
    x = inX
    y = inY
    holeDiameter = inHoleDiameter
    super.init ()
  }

  //····················································································································

