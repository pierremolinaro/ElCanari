final class MergerViaShape : EBSimpleClass {

  //····················································································································

  let x : Int
  let y : Int
  let holeDiameter : Int
  let padDiameter : Int

  //····················································································································

  init (x inX : Int,
        y inY : Int,
        holeDiameter inHoleDiameter : Int,
        padDiameter inPadDiameter : Int) {
    x = inX
    y = inY
    holeDiameter = inHoleDiameter
    padDiameter = inPadDiameter
    super.init ()
  }

  //····················································································································

}

