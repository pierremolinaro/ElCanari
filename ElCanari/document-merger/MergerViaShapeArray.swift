import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   MergerViaShapeArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

final class MergerViaShapeArray : EBSimpleClass {

  //····················································································································

  let viaArray : [MergerViaShape]

  //····················································································································

  init (_ inArray : [MergerViaShape]) {
    viaArray = inArray
    super.init ()
  }

  //····················································································································

  override var description : String {
    get {
      return "MergerViaShapeArray " + String (viaArray.count)
    }
  }

  //····················································································································

}

