import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

fileprivate var gPanel : OpenPanelDelegateForUpdatingBoardModels?

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class OpenPanelDelegateForUpdatingBoardModels : EBSimpleClass, NSOpenSavePanelDelegate {

  //····················································································································
  //   PROPERTIES
  //····················································································································

  let mBoardModelName : String

  //····················································································································
  //   INIT
  //····················································································································

  init (_ boardModelName : String) {
    mBoardModelName = boardModelName
    super.init ()
    gPanel = self // MANDATORY! This object is set to NSOpenPanel delegate that DOES NOT retain it
  }

  //····················································································································
  //   DELEGATE METHOD
  //····················································································································

  func panel (_ sender: Any, shouldEnable url: URL) -> Bool {
    let fileName = url.path.lastPathComponent.deletingPathExtension
    // NSLog ("\(fileName)")
    return mBoardModelName == fileName
  }

  //····················································································································

}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

func releaseOpenPanelDelegateForUdatingBoardModel () {
  gPanel = nil
}

