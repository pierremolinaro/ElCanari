import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   MergerPad
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

final class MergerPad : EBSimpleClass {

  //····················································································································

  let x : Int
  let y : Int
  let width : Int
  let height : Int
  let holeDiameter : Int
  let shape : PadShape
  let rotation : Double

  //····················································································································

  init (x inX : Int,
        y inY : Int,
        width inWidth : Int,
        height inHeight : Int,
        holeDiameter inHoleDiameter : Int,
        shape inShape : PadShape,
        rotation inRotation : Double) {
    x = inX
    y = inY
    width = inWidth
    height = inHeight
    holeDiameter = inHoleDiameter
    shape = inShape
    rotation = inRotation
    super.init ()
  }

  //····················································································································

}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   MergerPadArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

final class MergerPadArray : EBSimpleClass {

  //····················································································································

  let padArray : [MergerPad]

  //····················································································································

  init (_ inArray : [MergerPad]) {
    padArray = inArray
    super.init ()
  }

  //····················································································································

  override var description : String {
    get {
      return "MergerPadArray " + String (padArray.count)
    }
  }

  //····················································································································

}

