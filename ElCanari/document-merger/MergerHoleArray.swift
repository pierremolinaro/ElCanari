import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   MergerHoleArray
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

final class MergerHoleArray : EBSimpleClass {

  //····················································································································

  let holeArray : [MergerHole]

  //····················································································································

  init (_ inArray : [MergerHole]) {
    holeArray = inArray
    super.init ()
  }

  //····················································································································

  override var description : String {
    get {
      return "MergerHoleArray " + String (holeArray.count)
    }
  }

  //····················································································································

}

