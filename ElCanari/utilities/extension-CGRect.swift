import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   EXTENSION CGRect
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension CGRect {

  //····················································································································

  init (point p1: CGPoint, point p2: CGPoint) {
    self.init ()
    self.origin = CGPoint (x: min (p1.x, p2.x), y: min (p1.y, p2.y))
    self.size = CGSize (width: abs (p1.x - p2.x), height: abs (p1.y - p2.y))
  }

  //····················································································································

}

