import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension String {

  //····················································································································

  var unicodeArray : [UnicodeScalar] {
    var array = [UnicodeScalar] ()
    for scalar in self.unicodeScalars {
      array.append (scalar)
    }
    return array
  }

  //····················································································································

}

