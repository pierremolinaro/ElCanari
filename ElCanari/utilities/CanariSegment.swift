final class CanariSegment : EBObject {

  //····················································································································

  let x1 : Int
  let y1 : Int
  let x2 : Int
  let y2 : Int
  let width : Int

  //····················································································································

  init (x1 inX1 : Int, y1 inY1 : Int, x2 inX2 : Int, y2 inY2 : Int, width inWidth : Int) {
    x1 = inX1
    y1 = inY1
    x2 = inX2
    y2 = inY2
    width = inWidth
    super.init ()
  }

  //····················································································································

}

