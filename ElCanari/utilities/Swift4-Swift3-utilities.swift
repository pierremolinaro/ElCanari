import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//  NSImage
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

func imageNamed (_ inName : String) ->  NSImage {
  #if swift(>=4)
    return NSImage (named: NSImage.Name (inName))!
  #else
    return NSImage (named: inName)!
  #endif
}

