import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   StopModalButton
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class StopModalButton : EBButton {

  //····················································································································

  override func awakeFromNib () {
    super.awakeFromNib ()
    self.target = self
    self.action = #selector (StopModalButton.stopModalAction (_:))
  }

  //····················································································································

  @objc func stopModalAction (_ sender : Any?) {
    if let window = self.window {
      window.performClose (sender)
    }
 //   NSApp.stop (sender)
  }

  //····················································································································

}

