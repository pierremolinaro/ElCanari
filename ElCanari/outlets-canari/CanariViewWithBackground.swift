import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   CanariViewWithBackground
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class CanariViewWithBackground : NSView {

  //····················································································································

  override func draw(_ dirtyRect: NSRect) {
    NSColor.windowBackgroundColor.setFill ()
    __NSRectFill (dirtyRect)
  }

  //····················································································································

  override var isOpaque : Bool {
    return true
  }

  //····················································································································


}

