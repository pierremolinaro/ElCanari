import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

let CANARI_ISSUE_HILITE_SIZE : CGFloat = milsToCocoaUnit (25.0) * 4.0

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   CanariIssueKind
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

enum CanariIssueKind {
  case warning
  case error
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   CanariIssue
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

struct CanariIssue : Hashable {

  //····················································································································
  //   Properties
  //····················································································································

  let mMessage : String
  let mKind : CanariIssueKind
  let mPath : NSBezierPath

  //····················································································································
  //   Init
  //····················································································································

  init (kind inKind : CanariIssueKind,
        message inMessage : String,
        path inBezierPath : NSBezierPath = NSBezierPath ()) {
    mMessage = inMessage
    mPath = inBezierPath
    mKind = inKind
  }

  //····················································································································
  //   Protocol Equatable
  //····················································································································

  public static func == (lhs: CanariIssue, rhs: CanariIssue) -> Bool {
    return (lhs.mMessage == rhs.mMessage) && (lhs.mKind == rhs.mKind) && (lhs.mPath == rhs.mPath)
  }

  //····················································································································
  // Bezier path center point
  //····················································································································

  var center : NSPoint {
    if self.mPath.elementCount == 0 {
      return NSPoint ()
    }else{
      return NSPoint (x: self.mPath.bounds.midX, y: self.mPath.bounds.midY)
    }
  }

  //····················································································································
  // Sorting
  //····················································································································

  public static func displaySortingCompare (lhs: CanariIssue, rhs: CanariIssue) -> Bool {
    let lP = lhs.center
    let rP = rhs.center
    return (lP.x < rP.x) || ((lP.x == rP.x) && (lP.y < rP.y))
  }

  //····················································································································

}


